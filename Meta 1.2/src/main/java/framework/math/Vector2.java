package framework.math;

/**
 * Contiene dos variables para almacenar datos de manera binaria (de dos), se accede de manera directa
 * @author <a href="https://github.com/medina1402">Abraham Medina Carrillo</a>
 */

public class Vector2 {
    public float x;
    public float y;

    /**
     * @param x Valor en x
     * @param y Valor en y
     */

    public Vector2(float x, float y) {
        this.x = x;
        this.y = y;
    }
}
